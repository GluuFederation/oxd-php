require_once 'Client_OXD.php';
require_once 'Obtain_trait.php';

class Obtain_aat extends Client_oxd
{
    use Obtain_trait;
    /**start parameter for response!**/
        public static $resp_aat_token;
        public static $resp_aat_refresh_token;
        public static $resp_expires_in_seconds;
        public static $resp_authorization_code;
        public static $resp_scope;
    /**end response parameter**/

    public function __construct()
    {
        /**
         * Obtain_aat constructor.
         */
        parent::__construct(); // TODO: Change the autogenerated stub
    }

    public function getReqDiscoveryUrl()
    {
        return $this->req_discovery_url;
    }
    /*
     * @param static
    */
    public function setRespParam(){
        self::$resp_aat_token = $this->getRespData()->aat_token;
        self::$resp_aat_refresh_token = $this->getRespData()->aat_refresh_token;
        self::$resp_expires_in_seconds = $this->getRespData()->expires_in_seconds;
        self::$resp_authorization_code = $this->getRespData()->authorization_code;
        self::$resp_scope = $this->getRespData()->scope;
    }

    public function setCommand()
    {
        $this->command = 'obtain_aat';
    }
}
